package chapter02;

public class VariableExample {
	public static void main(String[] args) {
		// byte: ~128 ~ 127까지 저장 가능
		int a; //변수의 선언
		a = 10; // 변수의 초기화
		System.out.println(a);
		
		int b = 100; //변수 선언과 동시에 초기화
		System.out.println(b);
		
		a = 10000; 
		System.out.println(a);
	}
}
